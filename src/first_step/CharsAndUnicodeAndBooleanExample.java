package first_step;

public class CharsAndUnicodeAndBooleanExample {
    public static void main(String[] args) {
        char myChar = 'D';

        /**
         * For unicode reference go to
         *
         * uncode-table.com
         */
        char myUnicodeChar = '\u0044';
        System.out.println("myChar: " + myChar + " myUnicodeChar: " + myUnicodeChar);

        boolean myTrueBooleanValue = true;
        boolean myFalseBooleanValue = false;
        System.out.println("myTrueBoolean Value Is: " + myTrueBooleanValue + " myFalseBooleanValue Is: " + myFalseBooleanValue );


    }
}
