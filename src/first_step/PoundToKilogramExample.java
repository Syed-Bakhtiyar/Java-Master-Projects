package first_step;

public class PoundToKilogramExample {
    public static void main(String[] args) {
        double poundValue = 305;
        double kilogramEqualsTo1Pound = 0.45359237;
        double poundToKilogramResult = poundValue * kilogramEqualsTo1Pound;
        System.out.println(poundValue+ " is = " + poundToKilogramResult + " Kilogram");
    }
}
